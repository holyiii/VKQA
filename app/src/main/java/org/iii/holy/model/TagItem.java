package org.iii.holy.model;


public class TagItem {
	private Long id;
	private String name;
	public TagItem() {}

	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
